    function autocompleteMatch(input) {
        if (input == '') {
            return [];
        }
        var reg = new RegExp(input, "i");
        return search_terms.filter(function(term) {
            if (term.match(reg)) {
            return term;
            }
        });
    }
    
    function showResults(val) {
        res = $("#results-display-" + currentIndex);
        res2 = $("#results-list");
        res.html('');
        let list = '';
        let t_c = 0;
        let terms = autocompleteMatch(val);
        for (i=0; i<terms.length; i++) {
            if(t_c > 3) {
                break;
            }
            else {
                if(i % 2 == 0) {
                    list += '<li class="r-bar results-bar-1" style="font-family:Georgia, Times New Roman, Times, serif;text-align:center;font-size:20px;">' + terms[i] + '</li>';
                }
                else {
                    list += '<li class="r-bar results-bar-2" style="font-family:Georgia, Times New Roman, Times, serif;text-align:center;font-size:20px;">' + terms[i] + '</li>';
                }
                t_c++;
            }
        }

        res2.html(list);

        $(".r-bar").click(function() {
            $("#name-guess").val($(this).text());
        });
    }

